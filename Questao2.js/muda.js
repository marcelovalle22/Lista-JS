function mouseOver(color){
    switch(color){
        case 'orange':
        document.getElementById('').style.backgroundColor='orange';
        break;

        case 'red':
        document.getElementById('').style.backgroundColor='red';
        break;

        case 'black':
        document.getElementById('').style.backgroundColor='black';
        break;

        case 'blue':
        document.getElementById('').style.backgroundColor='blue';
        break;

        default:
        document.getElementById('').style.backgroundColor= 'navy blue';
        break;
    }
}